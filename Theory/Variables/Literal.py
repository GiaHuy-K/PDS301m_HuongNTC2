# Trong Python, literal hay còn gọi là hằng số, là cách thể hiện một giá trị cố định trực tiếp trong mã nguồn. 
# Khác với biến, literal (ví dụ: 123, 4.3, “Xin chào“) là những giá trị tĩnh hoặc hằng số, 
# không thay đổi trong suốt quá trình chạy của chương trình hay ứng dụng.
# Vi du 
x = 15 # 15 là một literal kiểu int
y = x*2 # day khong phai la literal
z = int(10) # 10 la mot literal kieu int nhung khai bao gian tiep bang ham int(), neu khai bao truc tiep nhu z = 10 thi 10 moi la literal


# Các loại Literal phổ biến trong Python
# Python cung cấp một số loại literal khác nhau, mỗi loại dùng để biểu diễn các kiểu dữ liệu khác nhau.
# Dưới đây là danh sách các loại literal phổ biến mà chúng ta sẽ cùng tìm hiểu:

# Literal số nguyên (Integer Literal)
# Literal số thực (Float Literal)
# Literal số phức (Complex Literal)
# Literal chuỗi (String Literal)
# Literal danh sách (List Literal)
# Literal tuple (Tuple Literal)
# Literal từ điển (Dictionary Literal)


# Literal Tuple
T1 = (1, "Lan", 80.2, False)
print (T1, type(T1))
# Literal Tuple không cần dấu ngoặc đơn
T2 = 2, "Hùng", 92.7, True
