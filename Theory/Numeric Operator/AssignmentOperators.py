# Trong Python, ký hiệu = (dấu bằng) được định nghĩa là toán tử gán. 
# Toán tử này có vai trò gán giá trị của biểu thức ở bên phải cho một biến duy nhất ở bên trái. 
# Cần đặc biệt lưu ý rằng, ký hiệu = trong lập trình (và Python nói riêng) không giống với cách sử dụng của nó trong toán học. 
# Trong toán học, dấu = thể hiện sự bằng nhau giữa hai biểu thức ở hai vế, còn trong lập trình, = là lệnh gán giá trị.
a = 5
b = 10
c = a
a = b 
b = c
print(a)
print(b)
